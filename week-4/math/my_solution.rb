def add(num_1, num_2)
 sum = num_1 + num_2
 return sum
end

def subtract(num_1, num_2)
 diff = num_1 - num_2
 return diff
end

def multiply(num_1, num_2)
 multi = num_1 * num_2
 return multi
end

def divide(num_1, num_2)
 remainder = num_1.to_f / num_2
 return remainder
end